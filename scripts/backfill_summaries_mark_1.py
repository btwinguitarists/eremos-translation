def main():
    verses = json.loads(CHAPTER.read_text(encoding="utf-8"))
    added = 0
    for v in verses:
        n = v["verse"]
        if n in SUMMARIES:
            if v["translation"].get("thai_summary"):
                print(f"  skip {v['reference']}: already has summary")
                continue
            v["translation"]["thai_summary"] = SUMMARIES[n]
            added += 1
    CHAPTER.write_text(json.dumps(verses, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"\nMark 1: +{added} thai_summary entries on {len(verses)} verses.")


if __name__ == "__main__":
    main()
